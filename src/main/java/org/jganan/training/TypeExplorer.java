package org.jganan.training;

public class TypeExplorer {
    static final double PI = 3.14159;

    public static void main() {
        byte age = 25;
        short year = 2024;
        int distance = 384400;
        long worldPop = 8100000000L;
        float temp = 36.6f;
        double pi = 3.14159265;
        boolean alive = true;
        char grade = 'A';

        System.out.println("byte: " + age);
        System.out.println("short: " + year);
        System.out.println("int: " + distance);
        System.out.println("long: " + worldPop);
        System.out.println("float: " + temp);
        System.out.println("double: " + pi);
        System.out.println("boolean: " + alive);
        System.out.println("char: " + grade);
        System.out.println("PI constant: " + PI);
    }
}
